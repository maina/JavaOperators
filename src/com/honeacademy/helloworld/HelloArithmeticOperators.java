package com.honeacademy.helloworld;
/**
 * 
 * @author james
 * Class to demonstrate arithmetic operators in Java
 *
 */
public class HelloArithmeticOperators {

	public static void main(String[] args) {
		 int i = 10;
         int j = 20;
         System.out.println(String.format("Addition %d", i+j));
         System.out.println(String.format("Subtraction %d", i-j));
         System.out.println(String.format("Multiplication %d", i*j));
         System.out.println(String.format("Division %d", i*j));
         System.out.println(String.format("Modulus %d", i%j));

	}

}
